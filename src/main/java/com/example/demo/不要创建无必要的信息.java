public class 不要创建无必要的信息 {
    public static void main(String[] args) {
        long start = System.currentTimeMillis();
        long a = 0L;
        for (int i = 0; i < Integer.MAX_VALUE; i++) {
            a++;
        }
        long end = System.currentTimeMillis();
        System.out.println("时间："+(end - start));
    }
}
